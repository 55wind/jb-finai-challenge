"""제출용 기능명세서(.docx)에 피드백 반영 6건을 동기화한다.

기존 docx를 열어 표3(기능)·표4(흐름)·표7(변경이력)을 갱신하고,
표5(향후)·표6(부록)은 손으로 다듬어진 풍부한 내용을 보존하기 위해
대상 문자열만 run 단위로 표적 치환한다. 표0(팀정보)·표2(아키텍처)는 미수정.
"""
from docx import Document
from docx.shared import Pt

PATH = "docs/submission/JB준법코파일럿_기능명세서_제출.docx"
d = Document(PATH)
T = d.tables


def set_cell(cell, text, size=8.5, bold=False):
    cell.text = ""
    r = cell.paragraphs[0].add_run(str(text))
    r.font.size = Pt(size)
    r.bold = bold


def set_cell_lines(cell, lines, size=9):
    cell.text = ""
    for i, ln in enumerate(lines):
        p = cell.paragraphs[0] if i == 0 else cell.add_paragraph()
        r = p.add_run(ln)
        r.font.size = Pt(size)


def replace_in_runs(cell, old, new):
    """셀 내 run 중 old를 포함한 run의 텍스트만 치환(서식·나머지 내용 보존)."""
    hit = 0
    for p in cell.paragraphs:
        for r in p.runs:
            if old in r.text:
                r.text = r.text.replace(old, new)
                hit += 1
    return hit


